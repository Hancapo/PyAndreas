class KEY:
    """Virtual-key constants (subset of the Win32 VK_* table)."""

    LBUTTON = 0x01
    RBUTTON = 0x02
    MBUTTON = 0x04
    BACKSPACE = 0x08
    TAB = 0x09
    ENTER = 0x0D
    SHIFT = 0x10
    CTRL = 0x11
    ALT = 0x12
    PAUSE = 0x13
    CAPSLOCK = 0x14
    ESCAPE = 0x1B
    SPACE = 0x20
    PAGEUP = 0x21
    PAGEDOWN = 0x22
    END = 0x23
    HOME = 0x24
    LEFT = 0x25
    UP = 0x26
    RIGHT = 0x27
    DOWN = 0x28
    INSERT = 0x2D
    DELETE = 0x2E

    N0, N1, N2, N3, N4 = 0x30, 0x31, 0x32, 0x33, 0x34
    N5, N6, N7, N8, N9 = 0x35, 0x36, 0x37, 0x38, 0x39

    A, B, C, D, E, F, G = 0x41, 0x42, 0x43, 0x44, 0x45, 0x46, 0x47
    H, I, J, K, L, M, N = 0x48, 0x49, 0x4A, 0x4B, 0x4C, 0x4D, 0x4E
    O, P, Q, R, S, T, U = 0x4F, 0x50, 0x51, 0x52, 0x53, 0x54, 0x55
    V, W, X, Y, Z = 0x56, 0x57, 0x58, 0x59, 0x5A

    NUMPAD0 = 0x60
    NUMPAD1 = 0x61
    NUMPAD2 = 0x62
    NUMPAD3 = 0x63
    NUMPAD4 = 0x64
    NUMPAD5 = 0x65
    NUMPAD6 = 0x66
    NUMPAD7 = 0x67
    NUMPAD8 = 0x68
    NUMPAD9 = 0x69
    MULTIPLY = 0x6A
    ADD = 0x6B
    SUBTRACT = 0x6D
    DECIMAL = 0x6E
    DIVIDE = 0x6F

    F1 = 0x70
    F2 = 0x71
    F3 = 0x72
    F4 = 0x73
    F5 = 0x74
    F6 = 0x75
    F7 = 0x76
    F8 = 0x77
    F9 = 0x78
    F10 = 0x79
    F11 = 0x7A
    F12 = 0x7B

    LSHIFT = 0xA0
    RSHIFT = 0xA1
    LCTRL = 0xA2
    RCTRL = 0xA3
